package com.neuedu.demo.tool;

public class Pair{
	private Object value1;
	private Object value2;
	public Pair(Object value1, Object value2) {
		super();
		this.value1 = value1;
		this.value2 = value2;
	}
	public Object getValue1() {
		return value1;
	}
	public Object getValue2() {
		return value2;
	}
}