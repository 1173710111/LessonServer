package com.neuedu.demo.tool;

public class PairLongs{
	private Long value1;
	private Long value2;
	public PairLongs(Long value1, Long value2) {
		super();
		this.value1 = value1;
		this.value2 = value2;
	}
	public Long getValue1() {
		return value1;
	}
	public Long getValue2() {
		return value2;
	}
}